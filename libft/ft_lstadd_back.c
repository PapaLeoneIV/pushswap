#include "libft.h"

void	ft_lstadd_back(t_list **lst, t_list *new)
{
	t_list		*ptrlst;

	if (*lst == NULL)
	{
		*lst = new;
		return ;
	}
	ptrlst = *lst;
	while (ptrlst->next != NULL)
		ptrlst = ptrlst->next;
	ptrlst->next = new;
}
